    <span class="T1">Boutons :</span>
  </h2>
  <p class="P1">
    <span class="T2">Bouton :</span>
    <span class="T1"> bouton simple, le champ peux contenir du javascript lié a ce bouton</span>
  </p>
  <p class="P1">
    <span class="T2">Bouton vers page :</span>
    <span class="T1"> bouton clickable pour changer de page</span>
  </p>
  <p class="P2">
    <span class="T1">url de la page.</span>
  </p>
  <p class="P1">
    <span class="T2">Bouton submit :</span>
    <span class="T1"> bouton de soumission de formulaire, detecte le context d'execution (AJAX ou non)</span>
  </p>
  <p class="P2">
    <span class="T1">pre=javascript a effectuer avant le submit. (defaut)</span>
  </p>
  <h2 class="P22">
    <a id="a__Associé_aux_données _">
      <span/>
    </a>
    <span class="T1">Associé aux données :</span>
  </h2>
  <p class="P3">
    <span class="T2">Select lié a table :</span>
    <span class="T1"> select allant chercher sont contenu dans la table « Tables »</span>
  </p>
  <p class="P2">table=Nom<span class="T1"> de la rubrique dans la table (defaut)</span>
  </p>
  <p class="P3">
    <span class="T2">Select lié a un fichier :</span>
    <span class="T1"> va chercher dans un fichier pour remplir le select</span>
  </p>
  <p class="P2">table=Nom de la table dans laquelle on cherche</p>
  <p class="P2">cle=colonne sur laquelle s'effectue la recherche. Prend une unique nom de colonne. Ce sera la valeur de clé du select</p>
  <p class="P2">libelle=libelle du select (utilise la syntaxe +)</p>
  <p class="P2">
    <span class="T13">condition=clause de recherche, si omise renvois toute la table.</span>
  </p>
  <p class="P14">
    <span class="T3">Recherche sur le champ :</span>
    <span class="T14"> créer un champ de recherche. Sur la perte de focus va regarder si un enregistrement de la table a la valeur saisie. Si un resultat existe, remplace l'enregistrement courant par celui trouvé.Si plusieurs resultat existe, renvois le premier. Si aucun enregistrement n'est trouvé, ne fais rien mise a part restauré la valeur initiale </span>
    <span class="T15">et informer l'utilisateur</span>
  </p>
  <p class="P15">
    <span class="T4">Fenetre de recherche : </span>
    <span class="T17">ouvre une fenetre de recherche, </span>
    <span class="T18">dans cette fenetre il faut entrer un critere de recherche, un tableau est présenté. La selection de la ligne met a jour le contenu du champ qui a ouvert la fenetre.</span>
  </p>
  <p class="P2">table=Nom de la table dans laquelle on cherche</p>
  <p class="P2">cle=cle de recherche, la requete est un commence par cle. On peux effectuer une recherche sur plusieurs champ en separant les champs par des , (exemple cle=p101,p103,p105)</p>
  <p class="P2">retour = contenu de la colonne qui sera placé dans le contenu du widget appelant. Si omis c'est la première clé déclaré dans cle.</p>
  <p class="P2">tri=nom du champ sur lequel s'effectue un tri</p>
  <p class="P2">cols=liste des colonnes devant apparaître dans le tableau de resultat séparé par des ,</p>
  <p class="P2">libelle=liste des libelle pour les colonnes decalré si dessus. Séparé par des ,</p>
